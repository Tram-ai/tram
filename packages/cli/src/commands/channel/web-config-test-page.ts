    '          state.error = error.message;',
    '          render();',
    '        }',
    '      }',
    '',
    '      async function pollBinding(bindingId) {',
    '        if (state.bindingTimer) {',
    '          clearTimeout(state.bindingTimer);',
    '          state.bindingTimer = null;',
