    '          const key = element.getAttribute("data-field-key");',
    '          const field = fieldMap.get(key);',
    '          if (!field) return;',
    '          const eventName = element.tagName === "SELECT" ? "change" : "input";',
    '          element.addEventListener(eventName, (event) => {',
