    '                  state.draft[extraField.key] = displayValue(extraField, undefined);',
    '                }',
    '              }',
    '              render();',
    '            }',
    '          });',
    '        });',
    '        document.getElementById("saveButton")?.addEventListener("click", saveChannel);',
    '        document.getElementById("deleteButton")?.addEventListener("click", deleteChannel);',
    '        document.getElementById("bindStartButton")?.addEventListener("click", startWeixinBind);',
    '        document.getElementById("clearWeixinButton")?.addEventListener("click", clearWeixinAccount);',
    '      }',
    '',
    '      async function loadMetadata(preserveDraft = false) {',
