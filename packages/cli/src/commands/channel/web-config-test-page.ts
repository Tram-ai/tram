    '        state.message = text.requestingWeChatQrCode;',
    '        state.messageType = "info";',
    '        render();',
    '        try {',
    '          const binding = await api("/api/weixin/bind", {',
    '            method: "POST",',
    '            body: JSON.stringify({ baseUrl: state.draft.baseUrl || undefined }),',
